export const calculateFulfillmentStats = (
  shipments: FulfillmentShipment[],
  ordersToShip: OrderToShip[]
) => ({
  pending: shipments.filter((s) => s.status === 'pending').length,
  inTransit: shipments.filter(
    (s) => s.status === 'in_transit' || s.status === 'picked_up'
  ).length,
  delivered: shipments.filter((s) => s.status === 'delivered').length,
  failed: shipments.filter(
    (s) => s.status === 'failed' || s.status === 'returned'
  ).length,
  waitingToShip: ordersToShip.length,
});
