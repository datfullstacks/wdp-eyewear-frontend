import type { OrderItem, OrderRecord } from '@/api/orders';

function item(
  id: string,
  name: string,
  quantity: number,
  unitPrice: number,
  opts?: Partial<OrderItem>
): OrderItem {
  return {
    id,
    name,
    type: 'frame',
    quantity,
    unitPrice,
    lineTotal: quantity * unitPrice,
    variant: 'Mặc định',
    preOrder: false,
    hasPrescription: false,
    prescriptionMode: 'none',
    orderMadeFromPrescriptionImage: false,
    prescription: null,
    ...opts,
  };
}

